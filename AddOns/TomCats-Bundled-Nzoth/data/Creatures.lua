for creatureID, creature in pairs(TCL.Data["Creatures_1"].records) do
	local key = creatureID .. "_DESC"
	local description = L[key]
	if description ~= key then
		if L.IsTranslationNeeded(key) then
			description = "(" .. L["Translator_Needed"] .. ")\n" .. description
		end
		if (creature["Locations"][1] or creature["Locations"][2]  or creature["Locations"][3]) then
			local locdesc = "Appears during:\n"
			if (creature["Locations"][3]) then
				locdesc = locdesc .. "  - Assault: Amathet Advance\n" -- quest ID 55350
			end
			if (creature["Locations"][2]) then
				locdesc = locdesc .. "  - Assault: Aqir Unearthed\n" -- quest ID 56308
			end
			if (creature["Locations"][1]) then
				locdesc = locdesc .. "  - Assault: The Black Empire\n" -- quest ID 57157
			end
			description = locdesc .. "\n" .. description
		else
			description = "It is unknown as to when and where this creature appears\n" .. description
		end
		creature["Description"] = description
	end
end

