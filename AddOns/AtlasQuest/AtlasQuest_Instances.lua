local includedMaps = false;
if GetFileIDFromPath("Interface\\AddOns\\Atlas\\Images\\Atlas_BattleforAzeroth\\NyalothaA") then
	includedMaps = true;
end

for path, instanceID in pairs(AQInstances.paths) do
	if (includedMaps == true) then
		-- Don't change the string if it's for an Atlas plugin
		if (strfind(path, "Atlas_Scenarios") == nil and strfind(path, "Atlas_Battlegrounds") == nil and strfind(path, "Atlas_OutdoorRaids") == nil) then
			path = gsub(path, "Images\\", "");
			path = "Atlas\\Images\\"..path;
		end
	end

	local fileID = GetFileIDFromPath("Interface\\AddOns\\"..path);
	if fileID then
		AQInstances.ids[fileID] = instanceID;
	end
end
